public class intexapmle {
    public static void main(String[] args) {
        // int형 변수 선언 및 초기화
        int a = 10;
        int b = 5;

        // int형 변수끼리 덧셈
        int sum = a + b;

        // 결과 출력
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("합계 = " + sum);
    }
}
